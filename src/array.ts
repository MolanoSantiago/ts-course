(() => {
  // Array numbers
  let numbers = [1, 2, 3, 4, 5];
  console.log(numbers);
  numbers.push(6);
  console.log(numbers);
  // Array strings
  let strings = ['a', 'b', 'c', 'd', 'e'];
  console.log(strings);
  strings.push('f');
  console.log(strings);

  // Array mixed
  let mixed = [1, 'a', 2, 'b', 3, 'c'];
  console.log(mixed);
  mixed.push(4);
  console.log(mixed);
  mixed.push('d');
  console.log(mixed);
})();
